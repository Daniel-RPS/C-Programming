#include <stdio.h>
#include <stdlib.h>

int main()
{

    int a = 0, b = 10;

    //Contando até 10
    while (a <= 10){
        printf("\n%d", a);

        //incremento
        a++; //a = a + 1;

    }

    //Contagem Regrassiva

    while (b >= 0){

        printf("\n%d", b);

        //incremento
        b--; //b = b - 1;

    }
    return 0;
}

